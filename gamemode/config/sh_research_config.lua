-- Возвращает данные текущего тех. уровня по количеству ОИ в банке
function SWExp.ResearchConfig.GetCurrentTechLevel(totalRP)
    local level = 1
    for lvl, data in pairs(SWExp.ResearchConfig.TechLevels) do
        if totalRP >= data.rp_threshold and lvl > level then
            level = lvl
        end
    end
    return math.min(level, SWExp.ResearchConfig.MaxTechLevel)
end

-- Возвращает порог следующего уровня (или nil если максимум/финал)
function SWExp.ResearchConfig.GetNextThreshold(currentLevel)
    local nextLvl = currentLevel + 1
    local data    = SWExp.ResearchConfig.TechLevels[nextLvl]
    return data and data.rp_threshold or nil
end

-- Возвращает true если уровень является финальным
function SWExp.ResearchConfig.IsFinalLevel(level)
    local data = SWExp.ResearchConfig.TechLevels[level]
    return data and data.isFinal == true
end
